// 기본 산술 연산
// 1. 두 숫자 10과 5를 변수 `a`와 `b`에 할당하세요.
// 2. 덧셈, 뺄셈, 곱셈, 나눗셈, 나머지 연산을 수행하고 결과를 각각의 변수에 저장하세요.
// 3. 각 연산의 결과를 콘솔에 출력하세요.

const a = 10;
const b = 5;

const result1 = a + b;
const result2 = a - b;
const result3 = a * b;
const result4 = a / b;
const result5 = a % b;

console.log(`${a} + ${b} = `, result1);
console.log(`${a} - ${b} = `, result2);
console.log(`${a} * ${b} = `, result3);
console.log(`${a} / ${b} = `, result4);
console.log(`${a} % ${b} = `, result5);
