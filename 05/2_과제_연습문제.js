// Q2. [연습문제 - 음수 걸러내기]
// 배열에 음수값이 섞여있습니다.
// 음수는 제외하고 양수들만 골라내어 합을 구하세요.
let numArr = [10, -10, 20, -30, 40];
let sum = 0;
for (const value of numArr) {
	if (value > 0) {
		sum = sum + value;
	}
}
console.log(sum);

