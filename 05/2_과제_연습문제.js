// Q4. [연습문제 - 암스트롱 수 구하기]
// - 100부터 999까지 암스트롱 수를 구하세요
// 암스트롱 수란?
// - 암스트롱의 수는 세 자리의 정수 중에서 각 자리의 수를 세 제곱한 수의 합과 자신이 같은 수를 말합니다.
// - 예를 들어 153 = 1 + 125 + 27 입니다.
// - 이와 같은 수를 암스트롱의 수라고 말합니다.
let num3 = 100;
while (num3 <= 999) {
	const n1 = (num3 - (num3 % 100)) / 100;
	const n2 = ((num3 % 100) - (num3 % 10)) / 10;
	const n3 = num3 % 10;

	if (num3 === n1 * n1 * n1 + n2 * n2 * n2 + n3 * n3 * n3) {
		console.log(num3);
	}
	num3++;
}
